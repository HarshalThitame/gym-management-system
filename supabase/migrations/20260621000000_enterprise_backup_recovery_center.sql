-- Super Admin full access, authenticated read access
do $$
declare
  tables text[] := array[
    'recovery_sessions', 'recovery_approvals', 'backup_replication',
    'backup_verifications', 'backup_storage_tiers', 'backup_security_events',
    'backup_compliance_reports', 'backup_schedules', 'backup_pitr_points'
  ];
  t text;
begin
  foreach t in array tables loop
    execute format('
      create policy "%s_super_admin" on %I for all to authenticated
      using (public.is_super_admin()) with check (public.is_super_admin());
    ', t, t);
    execute format('
      create policy "%s_read" on %I for select to authenticated using (true);
    ', t, t);
  end loop;
end $$;

