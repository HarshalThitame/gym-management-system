-- 2. RESTORE / RECOVERY SESSIONS
create table if not exists public.recovery_sessions (
  id uuid primary key default gen_random_uuid(),
  session_number integer generated by default as identity,
  recovery_type text not null check (recovery_type in (
    'full_platform', 'infrastructure', 'database', 'storage',
    'tenant', 'multi_tenant', 'franchise_group',
    'branch', 'branch_group',
    'membership_data', 'payment_data', 'invoice_data', 'attendance_data', 'booking_data', 'staff_data'
  )),
  status text not null default 'pending' check (status in (
    'pending', 'scope_selected', 'recovery_point_chosen', 'dependencies_validated',
    'impact_analyzed', 'approved', 'executing', 'validating', 'completed', 'failed', 'cancelled'
  )),
  organization_id uuid null references public.organizations(id) on delete set null,
  branch_id uuid null references public.branches(id) on delete set null,
  selected_backup_id uuid null references public.backup_jobs(id) on delete set null,
  recovery_point timestamptz not null default now(),
  restore_scope jsonb not null default '{}'::jsonb,
  records_affected integer null,
  storage_affected_bytes bigint null,
  estimated_downtime_minutes integer null,
  risk_assessment text null check (risk_assessment in ('low', 'medium', 'high', 'critical')),
  dependencies_validated boolean not null default false,
  impact_analysis jsonb not null default '{}'::jsonb,
  approval_level text null check (approval_level in ('level1_ops', 'level2_admin', 'level3_security', 'level4_executive')),
  approved_by uuid null references auth.users(id) on delete set null,
  approved_at timestamptz null,
  justification text null,
  executed_by uuid null references auth.users(id) on delete set null,
  executed_at timestamptz null,
  validated_at timestamptz null,
  validation_result text null check (validation_result in ('passed', 'failed', 'partial')),
  error_message text null,
  metadata jsonb not null default '{}'::jsonb,
  requested_by uuid null references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists recovery_sessions_status_idx on public.recovery_sessions (status, created_at desc);
create index if not exists recovery_sessions_org_idx on public.recovery_sessions (organization_id, created_at desc);
create index if not exists recovery_sessions_type_idx on public.recovery_sessions (recovery_type, created_at desc);
comment on table public.recovery_sessions is 'Enterprise restore/recovery sessions with full workflow tracking';

-- 3. RECOVERY APPROVAL WORKFLOW
