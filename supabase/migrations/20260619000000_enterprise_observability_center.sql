-- Enterprise Observability & Monitoring Center
-- Incident management, service registry, queue/cron monitoring, on-call, status page, error tracking

-- 1. SERVICE REGISTRY
create table if not exists public.observability_services (
  id uuid primary key default gen_random_uuid(),
  service_name text not null unique,
  service_version text not null default '1.0.0',
  environment text not null default 'production' check (environment in ('development', 'staging', 'production', 'dr')),
  service_type text not null check (service_type in ('api', 'web', 'worker', 'database', 'cache', 'queue', 'storage', 'ai', 'auth', 'payment', 'email', 'sms', 'notification')),
  status text not null default 'unknown' check (status in ('healthy', 'degraded', 'down', 'maintenance', 'unknown')),
  description text null,
  owner_team text null,
  owner_email text null,
  dependencies jsonb not null default '[]'::jsonb,
  metadata jsonb not null default '{}'::jsonb,
  last_health_check_at timestamptz null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists obs_services_type_idx on public.observability_services (service_type);
create index if not exists obs_services_status_idx on public.observability_services (status);
comment on table public.observability_services is 'Service registry for microservice architecture monitoring';

-- 2. SERVICE HEALTH SNAPSHOTS (time-series)
create table if not exists public.observability_service_health (
