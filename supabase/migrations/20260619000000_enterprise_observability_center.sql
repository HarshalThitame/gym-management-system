create policy "observability super admin full access"
on public.observability_services for all to authenticated
using (public.is_super_admin())
with check (public.is_super_admin());

create policy "observability read for authenticated"
on public.observability_services for select to authenticated
using (true);

-- Same pattern for all observability tables
do $$
declare
  tables text[] := array[
    'observability_service_health', 'observability_incidents', 'observability_incident_events',
    'observability_queues', 'observability_queue_metrics', 'observability_cron_jobs',
    'observability_cron_runs', 'observability_errors', 'observability_logs',
    'observability_oncall_schedules', 'observability_status_pages', 'observability_status_components',
    'observability_status_updates', 'observability_capacity_metrics', 'observability_tenant_health',
    'observability_escalation_policies'
  ];
  t text;
begin
  foreach t in array tables loop
    execute format('
      create policy "obs_%s_super_admin" on %I for all to authenticated
      using (public.is_super_admin()) with check (public.is_super_admin());
    ', replace(t, 'observability_', ''), t);
    execute format('
      create policy "obs_%s_read" on %I for select to authenticated using (true);
    ', replace(t, 'observability_', ''), t);
  end loop;
end $$;

