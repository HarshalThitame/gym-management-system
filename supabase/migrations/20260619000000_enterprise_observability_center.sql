-- Create monthly partitions for logs
do $$
declare
  start_date timestamptz := '2026-01-01'::timestamptz;
  end_date timestamptz := '2028-01-01'::timestamptz;
  d timestamptz := start_date;
  partition_name text;
begin
  while d < end_date loop
    partition_name := 'observability_logs_' || to_char(d, 'YYYYMM');
    execute format(
      'create table if not exists public.%I partition of public.observability_logs for values from (%L) to (%L)',
      partition_name, d, d + interval '1 month'
    );
    d := d + interval '1 month';
  end loop;
end $$;

create index if not exists obs_logs_level_idx on public.observability_logs (log_level, created_at desc);
