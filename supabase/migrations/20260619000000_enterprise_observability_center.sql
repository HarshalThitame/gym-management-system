  message text null,
  checked_at timestamptz not null default now()
);

create index if not exists obs_health_service_time_idx on public.observability_service_health (service_id, checked_at desc);
comment on table public.observability_service_health is 'Time-series health snapshots for each service';

-- 3. INCIDENT MANAGEMENT (Full lifecycle)
